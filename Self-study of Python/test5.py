# משימת קופה מתקדמת – "מבצע ל-VIPים"
# המשימה שלך:
#
# יש מילון של מוצרים ומחירים (כמו קודם).
#
# המשתמש מזין את שמו.
#
# לאחר מכן נשאל אם הוא לקוח VIP (yes או no)
#
# המשתמש בוחר מוצרים וכמויות (כמו קודם).
#
# בסוף:
#
# מוצגת קבלה מסודרת.
#
# אם המשתמש VIP → הוא מקבל 10% הנחה על הסכום הכולל.
#
# יש לציין בקבלה אם ההנחה הופעלה, ומה היה הסכום לפני ואחרי ההנחה.

